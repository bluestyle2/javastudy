package basic;

public class GallData {

	String name, src;

	public GallData(String name, String src) {
		super();
		this.name = name;
		this.src = src;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSrc() {
		return src;
	}

	public void setSrc(String src) {
		this.src = src;
	}
	
	
}
